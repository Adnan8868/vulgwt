// --- Download Action Generator Node ---
function compileDownloadButton(authorName, activeUrl, buttonColorStyle) {
    if (!activeUrl || activeUrl.trim() === "") {
        return `
            <button class="disabled-btn w-full py-2.5 bg-slate-100 text-slate-400 font-bold text-[11px] rounded-xl flex items-center justify-center gap-1.5 border border-slate-200" disabled>
                <i class="fa-solid fa-lock text-[9px]"></i> ${authorName}
            </button>
        `;
    } else {
        return `
            <a href="${activeUrl}" target="_blank" 
               class="w-full ${buttonColorStyle} text-white py-2.5 font-bold text-[11px] rounded-xl flex items-center justify-center gap-1.5 transition-all shadow-sm active:scale-95">
                <i class="fa-solid fa-cloud-arrow-down text-[10px]"></i> ${authorName}
            </a>
        `;
    }
}

// --- 3. LIVE FILTERS & SEARCH MODULE ---
function searchPastPapers() {
    const searchField = document.getElementById('paperSearchInput');
    const inputVal = searchField.value.toUpperCase().replace(/\s/g, '');
    
    const paperCards = Array.from(document.getElementsByClassName('paper-card'));
    const sectionHeaders = Array.from(document.getElementsByClassName('paper-section-header'));
    const notFoundWidget = document.getElementById('paperNotFoundMsg');
    
    const statLabel = document.getElementById('statLabel');
    const countDisplay = document.getElementById('totalSubjectsCount');
    const linksDisplay = document.getElementById('totalLinksCount');

    let matchingSubjects = 0;
    let matchingLinks = 0;

    paperCards.forEach(card => {
        const textContent = card.innerText.toUpperCase().replace(/\s/g, '');
        const isMatched = textContent.includes(inputVal);
        
        if (isMatched) {
            card.style.display = "flex";
            matchingSubjects++;
            const localLinks = card.querySelectorAll('a').length;
            matchingLinks += localLinks;
        } else {
            card.style.display = "none";
        }
    });

    sectionHeaders.forEach(header => {
        const deptKey = header.getAttribute('data-dept');
        const gridBox = document.getElementById(`grid-dept-${deptKey}`);
        const visibleCardsExist = Array.from(gridBox.children).some(child => child.style.display !== "none");
        header.style.display = visibleCardsExist ? "" : "none";
    });

    if (inputVal === "") {
        statLabel.innerText = "Total Subjects:";
        const baseData = pastPapersData[currentPaperTab];
        countDisplay.innerText = baseData.length < 10 ? `0${baseData.length}` : baseData.length;
        
        let initialLinks = 0;
        baseData.forEach(item => {
            const links = [item.obj_moazz, item.obj_waqar, item.obj_junaid, item.obj_vutopper, item.sub_moazz, item.sub_waqar, item.sub_junaid, item.sub_vutopper];
            links.forEach(l => { if(l && l.trim() !== "") initialLinks++; });
        });
        linksDisplay.innerText = initialLinks < 10 ? `0${initialLinks}` : initialLinks;
    } else {
        statLabel.innerText = "Search Result:";
        countDisplay.innerText = matchingSubjects < 10 ? `0${matchingSubjects}` : matchingSubjects;
        linksDisplay.innerText = matchingLinks < 10 ? `0${matchingLinks}` : matchingLinks;
    }

    if (matchingSubjects === 0 && inputVal !== "") {
        notFoundWidget.classList.remove('hidden');
    } else {
        notFoundWidget.classList.add('hidden');
    }
}

// --- 4. NAVIGATION CONTROLLER MUX ---
function switchPaperTab(tabTarget) {
    if (currentPaperTab === tabTarget) return;
    
    currentPaperTab = tabTarget;
    const midBtn = document.getElementById('midTabBtn');
    const finalBtn = document.getElementById('finalTabBtn');

    if (tabTarget === 'mid') {
        midBtn.classList.add('tab-active');
        midBtn.classList.remove('text-slate-300', 'hover:text-white');
        finalBtn.classList.remove('tab-active');
        finalBtn.classList.add('text-slate-300', 'hover:text-white');
    } else {
        finalBtn.classList.add('tab-active');
        finalBtn.classList.remove('text-slate-300', 'hover:text-white');
        midBtn.classList.remove('tab-active');
        midBtn.classList.add('text-slate-300', 'hover:text-white');
    }

    document.getElementById('paperSearchInput').value = "";
    document.getElementById('paperNotFoundMsg').classList.add('hidden');
    document.getElementById('statLabel').innerText = "Total Subjects:";
    
    renderPastPapers();
}

